/* harmony-data.js — проверенные задания тренажёра сингармонизма.
   Используются только слова из wordData и composeWords.
   Важно: слова с «и», которые в кириллице позже сменили ряд под влиянием
   палатализации (хонин→хөн, морин→мөрн — см. правило «Влияние «и»),
   в упражнения на «нейтральность и» не берём — иначе тренажёр учит
   противоположному тому, что сказано в правиле про палатализацию.
   Здесь «и» показана только на словах, где ряд в обеих формах не меняется. */

    function harmonyLookup(todo) {
        const cw = composeWords.find(w => w.todo === todo);
        if (cw) return { word: todo, modern: cw.modern, meaning: cw.meaning };
        for (const g of wordData) {
            const row = g.items.find(([t]) => t === todo);
            if (row) return { word: todo, modern: row[1], meaning: row[2] };
        }
        return null;
    }

    function hw(todo) {
        const x = harmonyLookup(todo);
        if (!x) throw new Error('harmony-data: слово не найдено в wordData/composeWords: ' + todo);
        return x;
    }

